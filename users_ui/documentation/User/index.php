<h2> Getting Started </h2>
<hr>

<p>
FoodLust is a website which serves a meeting place of all food enthusiast and food businesses in the Philippines. The purpose of the system is to provide a hub for food businesses which serves as the market for both buyers and sellers here in the Philippines. With so may competitors in the food business, the system could help each and every one on finding a possible buyer or rather supplier just by accessing our site. 
<br>
  Features like chatting, comments, likes could be seen in the system to improve and promote once business. It has little similarities with e-commerce sites like OLX, Lazada in which the users could interact and do business online. Having a place where you could meet people who has the same interest as you and knowing the new trends in the food industry could help every food enterepreneurs to grow in the business they live in. Carving for something? Nowhere to go? FoodLust gives you the way.
<br><br>
<img src="../Photos/homepage.png">
<br><br>
